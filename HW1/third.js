a = +prompt("Введите целое число a");
b = +prompt("Введите целое число b");
if (a >= 0 && b >= 0) {
  c = a - b;
  alert("Разность чисел a и b равна " + c);
} else if (a < 0 && b < 0) {
  c = a * b;
  alert("Произведение чисел a и b равно " + c);
} else {
  c = a + b;
  alert("Сумма чисел a и b равна " + c);
}